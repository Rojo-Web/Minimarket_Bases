    private void jBcFotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBcFotoActionPerformed
        JFileChooser Fotoc = new JFileChooser();
        Fotoc.showOpenDialog(this);
        archivoC = Fotoc.getSelectedFile();
        if (archivoC != null) {
            //Poner la imagen en el label
            ImageIcon camFoto = new ImageIcon(archivoC.getAbsolutePath());
            ImageIcon icono_proC = new ImageIcon(camFoto.getImage().getScaledInstance(lblFotomus.getWidth(), lblFotomus.getHeight(), Image.SCALE_DEFAULT));
            lblFotomus.setIcon(icono_proC);
        }
    }//GEN-LAST:event_jBcFotoActionPerformed

