    private void jTUserKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTUserKeyPressed

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            System.out.println("Entramos");
            for (int i = 0; i < 4; i++) {
                if (jTUser.getText().equalsIgnoreCase(usuarios[i])) {
                    pos_user = i;
                    jTcontraseña.setEnabled(true);
                    JOptionPane.showMessageDialog(rootPane, "Hola " + usuarios[i] + ", escribe tu contraseña");
                    jTUser.setEnabled(false);
                    jTUser.setForeground(Color.GRAY);
                    jTUser.setText(usuarios[i]);
                    System.out.println(pos_user);
                }
            }
            if (jTcontraseña.isEnabled() == false) {
                JOptionPane.showMessageDialog(rootPane, "Usuario incorrecto");
                jTUser.setForeground(Color.BLACK);
                jTUser.setText("");
            }
        }

    }//GEN-LAST:event_jTUserKeyPressed

