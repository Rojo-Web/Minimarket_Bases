    private void jTcontraseñaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTcontraseñaKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            System.out.println("Entramos clave");
                if (jTcontraseña.getText().equalsIgnoreCase(Contra[pos_user])) {

                    ImageIcon Pfoto = new ImageIcon(getClass().getResource(prfImg[pos_user]));
                    ImageIcon foto_mitad = new ImageIcon(Pfoto.getImage().getScaledInstance(ancho, alto, Image.SCALE_DEFAULT));
                    Object nivel = JOptionPane.showInputDialog(rootPane, "Cual es el Nivel del Usuario?", "Nivel del Usuario", JOptionPane.QUESTION_MESSAGE, foto_mitad,
                            new Object[]{"Administrador", "Usuario Comun"}, "Administrador");
                    JOptionPane.showMessageDialog(rootPane, nivel);
                    
                    jTcontraseña.setEnabled(false);

                    ImageIcon pFoto = new ImageIcon(getClass().getResource(prfImg[pos_user]));
                    ImageIcon icono = new ImageIcon(pFoto.getImage().getScaledInstance(jLfoto.getWidth(), jLfoto.getHeight(), Image.SCALE_DEFAULT));
                    jLfoto.setIcon(icono);
                    
                    Thread hilos = new Thread() {
                @Override
                public void run() {
                    for (int i = 0; i <= 100; i++) {
                        try {
                            Thread.sleep(10);
                            jPBlogin.setValue(i);
                            if (jPBlogin.getValue() == 100) {
                                dispose();
                                java.awt.EventQueue.invokeLater(new Runnable() {
                                    public void run() {
                                        new frmiPrincipal().setVisible(true);
                                    }
                                });

                            }
                        } catch (InterruptedException ex) {
                            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                }
            };
            hilos.start();

                }
            
            if (jTcontraseña.isEnabled() == true) {
                JOptionPane.showMessageDialog(rootPane, "contraseña incorrecto");
                jTcontraseña.setForeground(Color.BLACK);
                jTcontraseña.setText("");
            }

            
        }
    }//GEN-LAST:event_jTcontraseñaKeyPressed

